// Get gym by ID
export function getGymById(id: string): Gym | undefined {
  return GYMS.find(gym => gym.id === id);
}

// Get unique cities
export function getUniqueCities(): string[] {
  return [...new Set(GYMS.map(gym => gym.city))];
}

// Filter gyms
export function filterGyms(
  gyms: Gym[],
  region: string,
  type: string,
  searchQuery: string
): Gym[] {
  return gyms.filter(gym => {
    // Region filter
    if (region !== '全部') {
      const mainCities = ['台北', '新北', '桃園', '台中', '台南', '高雄'];
      if (region === '其他') {
        if (mainCities.includes(gym.city)) return false;
      } else {
        if (gym.city !== region) return false;
      }
    }
    
    // Type filter
    if (type !== '全部') {
      const typeMap: Record<string, string> = {
        '抱石': 'bouldering',
        '上攀': 'lead',
        '混合': 'mixed',
      };
      if (gym.type !== typeMap[type]) return false;
    }
    
    // Search query
    if (searchQuery) {
      const query = searchQuery.toLowerCase();
      const matchName = gym.name.toLowerCase().includes(query);
      const matchCity = gym.city.toLowerCase().includes(query);
      const matchDistrict = gym.district.toLowerCase().includes(query);
      if (!matchName && !matchCity && !matchDistrict) return false;
    }
    
    return true;
  });
}
